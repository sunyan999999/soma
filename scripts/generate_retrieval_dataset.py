def generate_passages() -> list[dict]:
    """从领域模板池生成 1000 条段落（每领域50条）"""
    passages = []
    domains = list(DOMAINS.keys())
    per_domain = 50  # 1000 / 20 领域

    for domain in domains:
        templates = DOMAINS[domain]
        # 1. 使用原始模板（最多25条）
        for template in templates:
            passages.append({
                "domain": domain,
                "text": template,
                "keywords": _extract_keywords(template),
            })

        # 2. 生成变体填充至 per_domain 条
        needed = per_domain - len(templates)
        for i in range(needed):
            a = templates[i % len(templates)]
            b = templates[(i + 3) % len(templates)]
            variant = f"{a.rstrip('。')}，同时{b.rstrip('。')}。"
            passages.append({
                "domain": domain,
                "text": variant,
                "keywords": _extract_keywords(variant),
            })

    # 确保恰好 1000 条 + 重新编号
    all_passages = []
    for i, p in enumerate(passages[:1000]):
        all_passages.append({
            "id": f"P{i+1:04d}",
            "domain": p["domain"],
            "text": p["text"],
            "keywords": p["keywords"],
        })
    return all_passages


def _extract_keywords(text: str) -> list[str]:
    """简单提取中文关键词（基于词长度和特征字符）"""
    import re
    # 提取中文词组（连续的中文字符）
    words = re.findall(r'[一-鿿]{2,6}', text)
    # 去重 + 限制数量
    seen = set()
    result = []
    for w in words:
        if w not in seen and len(w) >= 2:
            seen.add(w)
            result.append(w)
    return result[:8]


